package com.SpatialKey.entity;


public class ReturnPoint {
	
	private String latitude;
	private String longitude;
	private String TIV;
	private String occupancy;
	private String year;
	private String square;
	private String stories;
	private String construction;
	private String orgLatitude;
	private String orgLongitude;
	private Double distance;
	private Double euclideanDistance;
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getTIV() {
		return TIV;
	}
	public void setTIV(String tIV) {
		TIV = tIV;
	}
	public String getOccupancy() {
		return occupancy;
	}
	public void setOccupancy(String occupancy) {
		this.occupancy = occupancy;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSquare() {
		return square;
	}
	public void setSquare(String square) {
		this.square = square;
	}
	public String getStories() {
		return stories;
	}
	public void setStories(String stories) {
		this.stories = stories;
	}
	public String getConstruction() {
		return construction;
	}
	public void setConstruction(String construction) {
		this.construction = construction;
	}
	public String getOrgLatitude() {
		return orgLatitude;
	}
	public void setOrgLatitude(String orgLatitude) {
		this.orgLatitude = orgLatitude;
	}
	public String getOrgLongitude() {
		return orgLongitude;
	}
	public void setOrgLongitude(String orgLongitude) {
		this.orgLongitude = orgLongitude;
	}
	public Double getDistance() {
		return distance;
	}
	public void setDistance(Double distance) {
		this.distance = distance;
	}
	public Double getEuclideanDistance() {
		return euclideanDistance;
	}
	public void setEuclideanDistance(Double euclideanDistance) {
		this.euclideanDistance = euclideanDistance;
	}
	
}
